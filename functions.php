//Get Total Post Count
function oer_post_count($category, $taxonomy)
{
	$count = 0;
	$args = array(
	  'child_of' => $category,
	);

	$tax_terms = get_terms($taxonomy,$args);
	foreach ($tax_terms as $tax_term)
	{
		$count +=$tax_term->count;
	}
	return $count;
